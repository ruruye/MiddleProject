package kr.or.ddit.post.vo;

public class PostVO {
	
	private int post_id;
	private String mem_id;
	private String post_tit;
	private String post_cn;
	private String post_date;
	private int post_rdcnt;
	private String post_category;

	
	public int getPost_id() {
		return post_id;
	}
	public void setPost_id(int post_id) {
		this.post_id = post_id;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getPost_tit() {
		return post_tit;
	}
	public void setPost_tit(String post_tit) {
		this.post_tit = post_tit;
	}
	public String getPost_cn() {
		return post_cn;
	}
	public void setPost_cn(String post_cn) {
		this.post_cn = post_cn;
	}
	public String getPost_date() {
		return post_date;
	}
	public void setPost_date(String post_date) {
		this.post_date = post_date;
	}
	public int getPost_rdcnt() {
		return post_rdcnt;
	}
	public void setPost_rdcnt(int post_rdcnt) {
		this.post_rdcnt = post_rdcnt;
	}
	public String getPost_category() {
		return post_category;
	}
	public void setPost_category(String post_category) {
		this.post_category = post_category;
	}

	
	

}
